const fs = require('fs');
const path = require('path');

const srcDir = path.join(__dirname, 'src');
const relDir = path.join(__dirname, 'releases');

function bundle() {
    let bundleStr = "-- GAG2 Modular Bundled Edition\n";
    bundleStr += "-- Autogenerated local test bundle\n\n";
    
    // We will create a fake ModuleLoader that loads from an internal table
    bundleStr += `
local _BundleCache = {}
local ModuleLoader = {}
function ModuleLoader.load(path)
    if _BundleCache[path] then return _BundleCache[path]() end
    error("Module not found in bundle: " .. tostring(path))
end
\n`;
    
    function walk(dir) {
        const files = fs.readdirSync(dir);
        for (const f of files) {
            const p = path.join(dir, f);
            if (fs.statSync(p).isDirectory()) {
                walk(p);
            } else if (p.endsWith('.lua')) {
                const relPath = path.relative(__dirname, p).replace(/\\/g, '/');
                let code = fs.readFileSync(p, 'utf8');
                // Escape backslashes and brackets for long string
                // Better approach: wrap in a function definition!
                bundleStr += `_BundleCache["${relPath}"] = function()\n`;
                bundleStr += `${code}\n`;
                bundleStr += `end\n\n`;
            }
        }
    }
    walk(srcDir);
    
    // Now inject main.lua but strip out the loadRemote definition and ModuleLoader
    let mainSrc = fs.readFileSync(path.join(relDir, 'main.lua'), 'utf8');
    mainSrc = mainSrc.replace(/local function loadRemote.*?end\n/gs, '');
    mainSrc = mainSrc.replace(/local function loadRemoteFunction.*?end\n/gs, '');
    mainSrc = mainSrc.replace(/local ModuleLoader = loadRemote\(.*?\)/, '');
    mainSrc = mainSrc.replace(/local BASE = .*?\n/, '');
    
    bundleStr += mainSrc;
    
    fs.writeFileSync(path.join(__dirname, 'gag2_modular_bundled.lua'), bundleStr, 'utf8');
    console.log("Bundle created at gag2_modular_bundled.lua");
}

bundle();
